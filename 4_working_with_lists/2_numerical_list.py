# use of range() function in for loop`
print("Loop 1:")
for i in range(1,5):
   print(i)
# Here, 1 is starting point, and 5 is the limit
# This for loop will print from 1 to 4 
# not 5 because when it reaches 5 it will terminate

print("Loop 2:")
for  i in range(5):
   print(i)
# If we provide only one number then it will start with 0 and assume that number 
# as limit
